package Java_seminar.seminar_1;

public class task_1_models {
    public static int K1( int a,  int c) {
        int mult = 0;
        mult = mult + (a * c);
        return mult;
    }

    public static int K2( int a,  int d) {
        int sum = 0;
        sum = sum + (a + d);
        return sum;
    }
}
